#!/usr/bin/env bash
# =============================================================================
# worker.sh — Присоединение рабочей ноды к кластеру
# =============================================================================
#
# ЗАПУСКАЕТСЯ НА: k8s-worker1, k8s-worker2
# ЗАПУСКАЕТСЯ ПОСЛЕ: common.sh (на этой же ноде) + master.sh (на мастере)
# ЗАПУСКАЕТСЯ КАК: root
#
# ЧТО ДЕЛАЕТ:
#   1. Ждёт появления файла /vagrant/join-command.sh (создаётся мастером)
#   2. Проверяет, не присоединён ли уже воркер к кластеру
#   3. Выполняет команду kubeadm join
#
# ЗАЧЕМ ЖДАТЬ ФАЙЛ:
#   Vagrant запускает provisioner всех нод одновременно или в порядке описания.
#   master.sh занимает несколько минут. Если воркер попытается сделать join
#   раньше, чем мастер инициализировался — join упадёт с ошибкой.
#   Цикл ожидания решает эту проблему элегантно.
#
# КНИГИ:
#   EN: "Kubernetes in Action" — Marko Luksa, гл. 11
#   RU: «Kubernetes в действии» — Марко Лукша, ДМК Пресс, гл. 11
# =============================================================================

set -euo pipefail

echo ">>> [worker.sh] Начало. Нода: ${HOSTNAME}"

# =============================================================================
# ШАГ 1: Проверка — уже присоединены?
# =============================================================================
# /etc/kubernetes/kubelet.conf — создаётся после успешного kubeadm join.
# Если файл существует, нода уже в кластере — нет смысла делать join снова.
#
# Это обеспечивает ИДЕМПОТЕНТНОСТЬ: повторный vagrant provision не сломает
# уже работающий воркер.
if [ -f /etc/kubernetes/kubelet.conf ]; then
  echo ">>> Нода уже присоединена к кластеру (kubelet.conf существует). Пропускаем."
  exit 0
fi

# =============================================================================
# ШАГ 2: Ожидание команды join от мастера
# =============================================================================
# /vagrant/ — это общая папка, смонтированная Vagrant-ом.
# На хосте Windows это папка stage1/ (где лежит Vagrantfile).
# Внутри каждой ВМ она доступна как /vagrant/.
#
# master.sh создаёт /vagrant/join-command.sh в конце своей работы.
# Воркеры ждут этого файла, проверяя раз в 10 секунд.
#
# ТАЙМАУТ: 600 секунд (10 минут). Если мастер не инициализировался за это
# время — что-то пошло не так, прерываемся с ошибкой.
JOIN_FILE="/vagrant/join-command.sh"
MAX_WAIT=600
WAITED=0
INTERVAL=10

echo ">>> [ШАГ 2] Ожидание файла ${JOIN_FILE}..."
while [ ! -f "${JOIN_FILE}" ]; do
  if [ "${WAITED}" -ge "${MAX_WAIT}" ]; then
    echo "  ОШИБКА: Таймаут ожидания ${JOIN_FILE} (${MAX_WAIT} сек)." >&2
    echo "  Проверь статус мастера: vagrant ssh k8s-master -- systemctl status kubelet" >&2
    exit 1
  fi
  echo "  Жду... (${WAITED}/${MAX_WAIT} сек)"
  sleep "${INTERVAL}"
  WAITED=$((WAITED + INTERVAL))
done

echo "  Файл найден! (ждали ${WAITED} сек)"

# =============================================================================
# ШАГ 3: Присоединение к кластеру
# =============================================================================
# kubeadm join — подключает эту ноду к кластеру:
#   1. Связывается с API Server мастера по адресу из файла
#   2. Проверяет TLS-сертификат мастера (по CA-хэшу)
#   3. Аутентифицируется с помощью bootstrap-токена
#   4. Получает kubelet-конфиг и сертификаты
#   5. Регистрирует ноду в кластере
#
# Команда в файле имеет вид:
#   kubeadm join k8s-master:6443 \
#     --token <token> \
#     --discovery-token-ca-cert-hash sha256:<hash>
#
# --token: короткоживущий токен (24 часа), используется только для join.
#   После join воркер использует клиентские сертификаты.
#
# --discovery-token-ca-cert-hash: хэш CA-сертификата мастера.
#   Защита от подмены мастера при join (атака «человек посередине»).
echo ">>> [ШАГ 3] Выполнение kubeadm join..."
bash "${JOIN_FILE}"

echo ""
echo ">>> [worker.sh] Нода ${HOSTNAME} успешно присоединилась к кластеру!"
echo ""
echo "  Проверь на мастере:"
echo "  vagrant ssh k8s-master -- kubectl get nodes -o wide"
