#!/usr/bin/env bash
# =============================================================================
# worker.sh — Присоединение рабочей ноды к кластеру (Stage 2)
# =============================================================================
#
# ЗАПУСКАЕТСЯ НА: все worker-ноды (lab-k8s-worker1, lab-k8s-worker2, ...)
# ЗАПУСКАЕТСЯ ПОСЛЕ: common.sh (на этой же ноде) + master.sh (на мастере)
# ЗАПУСКАЕТСЯ КАК: root (через sudo)
#
# КОНТЕКСТ STAGE 2:
#   В Stage 2 provisioning всех нод стартует одновременно — Vagrant запускает
#   все машины параллельно. Это значит, что worker.sh может запуститься
#   ДО того, как master.sh закончил kubeadm init.
#
#   Поэтому worker.sh начинает с ожидания файла join-command.sh.
#   Мастер создаёт этот файл в конце своей работы — это сигнал:
#   «Я готов, можете подключаться».
#
# ЧТО ДЕЛАЕТ:
#   1. Ждёт появления и заполнения файла /vagrant/join-command.sh
#      (создаётся мастером через kubeadm token create --print-join-command)
#   2. Проверяет, не присоединён ли уже воркер к кластеру
#   3. Выполняет команду kubeadm join
#
# ЗАЧЕМ ИМЕННО ТАК:
#   Vagrant запускает provisioner всех нод одновременно.
#   master.sh занимает 3–8 минут (kubeadm init, Calico, Dashboard).
#   Если воркер попытается сделать join раньше, чем мастер инициализировался —
#   join упадёт с ошибой «connection refused».
#   Цикл ожидания решает эту проблему элегантно и без костылей.
#
# ОТЛИЧИЕ ОТ STAGE 1:
#   В Stage 1 worker.sh проверяет только наличие файла (-f).
#   Здесь проверяем, что файл ещё и не пустой (-s), потому что
#   kubeadm token create может ещё не завершиться, а файл уже создан.
#
# КНИГИ ДЛЯ ИЗУЧЕНИЯ ТЕМЫ:
#   EN: "Kubernetes in Action" — Marko Luksa, гл. 11 (Networking)
#   RU: «Kubernetes в действии» — Марко Лукша, ДМК Пресс, гл. 11
#   DOCS: https://kubernetes.io/docs/setup/production-environment/tools/kubeadm/create-cluster-kubeadm/
# =============================================================================

# Строгий режим bash:
#   -e: завершить при любой ошибке
#   -u: ошибка если переменная не определена
#   -o pipefail: ошибка если часть конвейера (pipe) упала
set -euo pipefail

# Путь к файлу с командой join.
# /vagrant/ — общая папка между хостом (Windows) и всеми ВМ.
# Vagrant монтирует рабочую директорию проекта в /vagrant/ каждой ВМ.
# Мастер записывает команду сюда → воркеры читают её отсюда.
JOIN_FILE="/vagrant/join-command.sh"

# =============================================================================
# ШАГ 1: Ожидание команды join от мастера
# =============================================================================
# Цикл ждёт, пока мастер создаст и заполнит join-command.sh.
#
# ПОЧЕМУ ЦИКЛ, А НЕ ОДНА ПРОВЕРКА:
#   Vagrant запускает provisioner параллельно. Мы не знаем, когда именно
#   мастер закончит kubeadm init. Цикл — это «мягкая синхронизация»
#   без жёсткой зависимости от порядка запуска.
#
# ПАРАМЕТРЫ ОЖИДАНИЯ:
#   60 итераций × 10 секунд = 600 секунд (10 минут) максимум.
#   Если мастер не инициализировался за 10 минут — что-то серьёзно
#   сломалось, и нет смысла ждать дальше.
#
# ПОЧЕМУ -s (size), А НЕ -f (file):
#   -f: файл существует (даже если пустой)
#   -s: файл существует И его размер > 0 (не пустой)
#   Это важно, потому что master.sh может создать файл, но ещё не
#   записать в него команду. -s гарантирует, что команда уже внутри.
echo ">>> [worker.sh] Начало. Нода: $(hostname)"
echo ">>> [worker.sh] Ожидание команды join от мастера..."

for _ in $(seq 1 60); do
  if [ -s "${JOIN_FILE}" ]; then
    break
  fi
  sleep 10
done

# =============================================================================
# ШАГ 2: Проверка — файл появился?
# =============================================================================
# Если цикл завершился, а файл всё ещё пустой или отсутствует —
# значит, мастер не смог инициализироваться за 10 минут.
# Это критическая ошибка: без join-команды воркер не может войти в кластер.
#
# ВОЗМОЖНЫЕ ПРИЧИНЫ:
#   - kubeadm init провалился на мастере (см. логи: vagrant ssh lab-k8s-master)
#   - Vagrant-синхронизация /vagrant/ сломана
#   - Нехватка ресурсов (CPU/RAM) — kubeadm init не успел за 10 минут
if [ ! -s "${JOIN_FILE}" ]; then
  echo ">>> [ОШИБКА] Файл join-command.sh не появился за 10 минут." >&2
  echo ">>> Возможные причины:" >&2
  echo ">>>   1. kubeadm init провалился на мастере" >&2
  echo ">>>   2. Нехватка ресурсов (CPU/RAM) на master-ноде" >&2
  echo ">>>   3. Проблема с синхронизацией /vagrant/" >&2
  echo ">>> Проверь логи мастера: vagrant ssh lab-k8s-master -- journalctl -u kubelet" >&2
  exit 1
fi

echo ">>> [worker.sh] Команда join получена от мастера."

# =============================================================================
# ШАГ 3: Присоединение к кластеру
# =============================================================================
# kubeadm join — подключает эту ноду к кластеру:
#   1. Связывается с API Server мастера по адресу из файла
#   2. Проверяет TLS-сертификат мастера (по CA-хэшу)
#   3. Аутентифицируется с помощью bootstrap-токена
#   4. Получает kubelet-конфиг и клиентские сертификаты
#   5. Регистрирует ноду в кластере
#
# Команда в файле имеет вид:
#   kubeadm join lab-k8s-master:6443 \
#     --token <token> \
#     --discovery-token-ca-cert-hash sha256:<hash>
#
# --token: короткоживущий токен (24 часа), используется только для join.
#   После join воркер использует клиентские сертификаты для общения.
#
# --discovery-token-ca-cert-hash: хэш CA-сертификата мастера.
#   Защита от подмены мастера при join (атака «человек посередине»).
#   Воркер проверяет, что подключается к настоящему мастеру, а не к
#   поддельному серверу, который перехватывает трафик.
#
# ИДЕМПОТЕНТНОСТЬ:
#   /etc/kubernetes/kubelet.conf создаётся после успешного kubeadm join.
#   Если файл уже есть — нода уже в кластере, повторный join не нужен.
#   Это позволяет безопасно перезапускать vagrant provision без поломок.
if [ ! -f /etc/kubernetes/kubelet.conf ]; then
  echo ">>> [worker.sh] Выполнение kubeadm join..."
  sudo bash "${JOIN_FILE}"
  echo ">>> [worker.sh] Нода $(hostname) успешно присоединилась к кластеру!"
else
  echo ">>> [worker.sh] Нода уже присоединена к кластеру (kubelet.conf существует). Пропускаем."
fi

echo ""
echo ">>> [worker.sh] Готово! Проверь на мастере:"
echo ">>>   vagrant ssh lab-k8s-master -- kubectl get nodes -o wide"
